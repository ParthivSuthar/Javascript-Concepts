// While Loop

let i = 0
while(i <= 10) {
    console.log(`Value of i is ${i}`);
    i += 2
}



// While loop in array

let myarr = ["Jio","Airtel","VI"]
let arr = 0
while(arr < myarr.length) {
    console.log(`The value myarr is ${myarr[arr]}`);
    arr = arr + 1
}



// Do While Loop

